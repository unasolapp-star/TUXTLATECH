<!-- Define el tipo de documento y la versión estándar de HTML (HTML5) -->
<!DOCTYPE html>
<!-- Inicia el documento HTML y establece el idioma principal del sitio en español -->
<html lang="es">
<!-- Contenedor principal reservado para los metadatos y configuraciones técnicas de la página -->
<head>
    <!-- Configura la codificación de caracteres a UTF-8 para soportar tildes y símbolos especiales -->
    <meta charset="UTF-8">
    <!-- Fuerza a navegadores heredados como Internet Explorer a utilizar su motor de renderizado más moderno -->
    <meta http-equiv="X-UA-Compatible" content="IE=edge">
    <!-- Ajusta la escala inicial y asegura que el área de dibujo sea adaptable a pantallas de dispositivos móviles -->
    <meta name="viewport" content="width=device-width, initial-scale=1.0">
    <!-- Define el título de la página que será exhibido en la pestaña superior del navegador -->
    <title>Document</title>
<!-- Cierra el bloque de declaración de metadatos -->
</head>
<!-- Inicia el cuerpo del documento, destinado a englobar los elementos visibles para el usuario -->
<body>
    <!-- Etiqueta de encabezado principal utilizada para realizar depuraciones o testeos simples del sistema backend -->
    <h1>información del usuario</h1>
<!-- Cierra el contenedor del contenido visible -->
</body>
<!-- Finaliza la estructura principal del documento web -->
</html>